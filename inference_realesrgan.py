def calculate_mse(image1, image2):
    assert image1.shape == image2.shape, "Images must have the same dimensions."
    mse = np.mean((image1 - image2) ** 2)
    return mse


def resize_image_to_smaller(image_a, image_b):
    b_height, b_width = image_b.shape[:2]  # (H, W)
    
    # Resize image A to match the size of image B
    resized_image_a = cv2.resize(image_a, (b_width, b_height))  # Note: OpenCV uses (W, H)
    
    return resized_image_a

def save_results_to_file(filename, mse_value,model_name):
    with open(filename, 'a') as file:  # Open the file in append mode
        file.write(f"Pixel-wise Mean Squared Error for {model_name} : {mse_value}\n")
    print(f"Results saved to {filename}")

